import type { ProviderId } from "@/lib/providers";

type Variant = "chip" | "mark";

type Props = {
  provider: ProviderId;
  size?: number;
  /** "chip" = brand-color square with white logo. "mark" = currentColor logo only. */
  variant?: Variant;
  className?: string;
  rounded?: number;
};

const BRAND: Record<ProviderId, { bg: string; fg: string }> = {
  twitter:   { bg: "#000000", fg: "#ffffff" },
  linkedin:  { bg: "#0a66c2", fg: "#ffffff" },
  instagram: { bg: "linear-gradient(135deg, #f09433 0%, #e6683c 25%, #dc2743 50%, #cc2366 75%, #bc1888 100%)", fg: "#ffffff" },
  facebook:  { bg: "#1877f2", fg: "#ffffff" },
  threads:   { bg: "#000000", fg: "#ffffff" },
  bluesky:   { bg: "#0a85ff", fg: "#ffffff" },
  mastodon:  { bg: "#6364ff", fg: "#ffffff" },
  tiktok:    { bg: "#000000", fg: "#ffffff" },
};

export function SocialLogo({
  provider,
  size = 20,
  variant = "mark",
  className = "",
  rounded,
}: Props) {
  const Logo = LOGOS[provider];
  if (!Logo) return null;

  if (variant === "mark") {
    return <Logo size={size} className={className} />;
  }

  // chip: square background with brand color, logo inside
  const brand = BRAND[provider];
  const innerSize = Math.round(size * 0.58);
  const radius = rounded ?? Math.round(size * 0.22);
  return (
    <span
      className={`inline-grid place-items-center shrink-0 ${className}`}
      style={{
        width: size,
        height: size,
        background: brand.bg,
        borderRadius: radius,
        color: brand.fg,
      }}
      aria-label={provider}
    >
      <Logo size={innerSize} />
    </span>
  );
}

